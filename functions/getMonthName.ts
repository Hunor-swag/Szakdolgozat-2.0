export function getMonthName(month: number) {
  switch (month) {
    case 1:
      return "január";
    case 2:
      return "február";
    case 3:
      return "március";
    case 4:
      return "április";
    case 5:
      return "május";
    case 6:
      return "június";
    case 7:
      return "július";
    case 8:
      return "augusztus";
    case 9:
      return "szeptember";
    case 10:
      return "október";
    case 11:
      return "november";
    case 12:
      return "december";
  }
}
